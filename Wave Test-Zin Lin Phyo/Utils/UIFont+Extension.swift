import Foundation
import UIKit

extension UIFont {
    
    public class var regularSmall: UIFont {
        return UIFont(name: "Helvetica", size: 12)!
    }
    
    public class var regularMedium: UIFont {
        return UIFont(name: "Helvetica", size: 16)!
    }
    
    public class var boldSmall: UIFont {
        return UIFont(name: "Helvetica-Bold", size: 12)!
    }
    
    public class var boldMedium: UIFont {
        return UIFont(name: "Helvetica-Bold", size: 16)!
    }
    
}
